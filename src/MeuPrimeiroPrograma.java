public class MeuPrimeiroPrograma {

    public static void main(String[] args) {
        sum(10, 5);
        sum(5, 10);
    }
    static void sum(int x, int y){
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x / y);
        System.out.println(x * y);
    }


}
